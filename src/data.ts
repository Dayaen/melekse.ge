// Helper to parse line string into tokens
export function parseLine(line: string): WordToken[] {
  const tokens: WordToken[] = [];
  let currentIndex = 0;

  while (currentIndex < line.length) {
    const startBracket = line.indexOf("[", currentIndex);
    if (startBracket === -1) {
      tokens.push({ text: line.substring(currentIndex) });
      break;
    }

    if (startBracket > currentIndex) {
      tokens.push({ text: line.substring(currentIndex, startBracket) });
    }

    const endBracket = line.indexOf("]", startBracket);
    if (endBracket === -1) {
      tokens.push({ text: line.substring(startBracket) });
      break;
    }

    const content = line.substring(startBracket + 1, endBracket);
    const parts = content.split("|");
    if (parts.length === 2) {
      tokens.push({ text: parts[0], tip: parts[1] });
    } else {
      tokens.push({ text: content });
    }

    currentIndex = endBracket + 1;
  }

  return tokens;
}

